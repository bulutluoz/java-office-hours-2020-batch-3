package day14;

public class AsciiValues {

	public static void main(String[] args) {
		/* 
		 * Write a Java program that prints ascii values of characters 	from ‘a’ to ‘z’	
		 */

		
		for (int i = 'a'; i <= 'z'; i++) {
			
			System.out.println( "Ascii value of " + (char)i + " = " + (int)i);
		}
		
		
		
	}

}